public class Main {
    public static void main(String[] args) {

        //Degişkenlerin tanımlanması.

        int A=10,B=20,C=10,D=40;

        //Eşittir operatörü.

        System.out.println(A==B);
        System.out.println(A==C);
        System.out.println(A==D);
        System.out.println(B==C);
        System.out.println(B==D);
        System.out.println(C==D);
        System.out.println("=================================");

        //Eşit değildir operatörü.

        System.out.println(A!=B);
        System.out.println(A!=C);
        System.out.println(A!=D);
        System.out.println(B!=C);
        System.out.println(B!=D);
        System.out.println(C!=D);
        System.out.println("=================================");

        //Büyüktür operatörü.

        System.out.println(A>B);
        System.out.println(A>C);
        System.out.println(A>D);
        System.out.println(B>C);
        System.out.println(B>D);
        System.out.println(C>D);
        System.out.println("=================================");

        //Küçüktür operatörü.

        System.out.println(A<B);
        System.out.println(A<C);
        System.out.println(A<D);
        System.out.println(B<C);
        System.out.println(B<D);
        System.out.println(C<D);
        System.out.println("=================================");

        //Büyüktür eşittir operatörü.

        System.out.println(A>=B);
        System.out.println(A>=C);
        System.out.println(A>=D);
        System.out.println(B>=C);
        System.out.println(B>=D);
        System.out.println(C>=D);
        System.out.println("=================================");

        //Küçüktür eşittir operatörü.

        System.out.println(A<=B);
        System.out.println(A<=C);
        System.out.println(A<=D);
        System.out.println(B<=C);
        System.out.println(B<=D);
        System.out.println(C<=D);
        System.out.println("=================================");
    }
}